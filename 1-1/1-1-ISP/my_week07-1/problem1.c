#include <stdio.h>

int main()
{
    int a;
    unsigned int b;
    
    char c;
    short d;
    long e;
    
    float f;
    double g;
    
    printf("size of int a: %ld\n", sizeof(a));
    printf("size of unsigned int b: %ld\n", sizeof(b));
    printf("size of char c: %ld\n", sizeof(c));
    printf("size of short d: %ld\n", sizeof(d));
    printf("size of long e: %ld\n", sizeof(e));
    printf("size of float f: %ld\n", sizeof(f));
    printf("size of double g: %ld\n", sizeof(g));
    

    return 0;
}
